		  AESK_UINT16toUINT8_LE(&Kumanda_Data.scaled_data[5], aesk_nrf24_data.TxBuffer, &index);
		  AESK_UINT16toUINT8_LE(&Kumanda_Data.scaled_data[6], aesk_nrf24_data.TxBuffer, &index);
		  aesk_nrf24_data.TxBuffer[++index] = Kumanda_Data.swtch[0];
		  aesk_nrf24_data.TxBuffer[++index] = Kumanda_Data.swtch[1];
		  aesk_nrf24_data.TxBuffer[++index] = Kumanda_Data.swtch[2];
