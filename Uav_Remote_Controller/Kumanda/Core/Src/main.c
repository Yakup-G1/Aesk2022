			  Kumanda_Data.swtch[2] = HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_7);
			  Kumanda_Data.swtch[3] = HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_6);

